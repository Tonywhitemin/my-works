
$(document).ready(function(){
    $("span.show_or_not").hide();
});
$(document).ready(function(){
    $("label.yes").click(function(){
    $("span.show_or_not").show();
    $("div.files_1").show();
    });
});
$(document).ready(function(){
    $("label.no").click(function(){
    $("span.show_or_not").hide();
    $("div.files_1").hide();
    $("div.files_2").hide();
    $("div.files_3").hide();
    });
});       
